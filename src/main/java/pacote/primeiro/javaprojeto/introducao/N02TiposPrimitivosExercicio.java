package pacote.primeiro.javaprojeto.introducao;

/*
* Crie variáveis para esses campos entre <> e imprima:
* Eu <nome>, morando no <endereço>, confrimo que recebi o salário
* de <salario>, na data <data>*/

public class N02TiposPrimitivosExercicio {
    public static void main(String[] args) {
        String nome = "Ulisses Messias";
        String endereco = "R. 7 de Abril, Bairro Norte, Cidade 29";
        double salario = 2750.00;
        String data = "05-09-2023";
        System.out.println("Eu, " + nome + " morando no endereço " + endereco + " confirmo que recebi " + salario + " reais em " + data);
    }
}
